print(10 + 3)
print(10 - 3)
print(10 * 3)  # It'll give the multiplication of two numbers
print(10 ** 3)  # 2nd value will be the power of 1st value
print(10 / 3)  # It'll give float value
print(10 // 3)  # It'll give int value
print(10 % 3)  # It'll give reminder of division

